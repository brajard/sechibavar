  
/***************************************************************************
                 module  E_emis_tempsol.h  -  description



Equivalent temperature or brightness temperature (Planck fonction inverse), 
Regression de Svendsen et al. (1990- AGM)
                          


output---> TB


 ***************************************************************************/

  //===========================================================================
  //                           forward
  forward (YREAL Rad)
  {

		YS1=  pow((Rad-7.84)/6.7975e-11,0.2);
		
   }
	

  //===========================================================================
  //                           backward
  backward (YREAL Rad)
  {
	double temp = (Rad-7.84)/6.7975e-11;

		YJ1I1 = (0.2 / 6.7975e-11 ) * pow(temp,-0.8);
		

  }

  //===========================================================================


  forward (YREAL kemis, YREAL emis_s, YREAL lwdown)
  {

		YS1=  kemis * emis_s + (1-kemis) * lwdown;

   }
	

  //===========================================================================
  //                           backward
  backward (YREAL kemis, YREAL emis_s, YREAL lwdown)
  {

		YJ1I1 = emis_s -lwdown ;
		YJ1I2 = kemis;
		YJ1I3 = (1-kemis);



  }

